# SLP1 -> IAST (for corpus search / display)
_S2I = {'A':'ā','I':'ī','U':'ū','f':'ṛ','F':'ṝ','x':'ḷ','X':'ḹ','E':'ai','O':'au',
        'M':'ṃ','H':'ḥ','N':'ṅ','Y':'ñ','w':'ṭ','W':'ṭh','q':'ḍ','Q':'ḍh','R':'ṇ',
        'S':'ś','z':'ṣ','K':'kh','G':'gh','C':'ch','J':'jh','T':'th','D':'dh',
        'P':'ph','B':'bh'}

def form_key(s):
    """Length-preserving SLP1 normalization for joining (keeps case/length;
    drops accents, slashes, hyphen bounds — NOT a naive NFD-strip-Mn)."""
    if not s:
        return ''
    s = unicodedata.normalize('NFC', s.strip())
    s = re.sub(r'[̀-ͯ]', '', s)
    s = s.replace('/', '').replace('\\', '').replace('|', '').replace(' ', '')
    return s.strip('-')

def slp1_to_ascii(s):
    """SLP1 -> diacritic-free lowercase roman, for FTS search."""
    iast = ''.join(_S2I.get(c, c) for c in form_key(s))
    nfd = unicodedata.normalize('NFD', iast)
    return ''.join(c for c in nfd if not unicodedata.combining(c)).lower()

# ---- dictionary index ----------------------------------------------------
# Which correctness-authority source files were present at the last load_index().
# FL7 (architecture audit 2026-07-02 / code review 2026-07-04): an absent evidence
# jsonl was silently skipped, so "no independent gloss" could mean EITHER "sources
# not built" OR "this headword is genuinely uncovered" — indistinguishable. This
# set lets build_card mark evidence_unavailable explicitly instead of degrading
# to an unmarked LLM-verdict.
SOURCES_PRESENT = set()


def evidence_status():
    """'ok' if >=1 independent correctness authority (INDEP) is loaded; else
    'evidence_unavailable' — meaning a 'no independent gloss' precheck reflects
    MISSING SOURCES, not an uncovered headword. Fail loud rather than silently
    degrade to the LLM verdict as if the evidence had been consulted and found
    nothing."""
    return 'ok' if any(c in SOURCES_PRESENT for c in INDEP) else 'evidence_unavailable'


def load_index():
    idx = defaultdict(lambda: defaultdict(list))
    SOURCES_PRESENT.clear()
    for code in INDEP + [REF]:
        path = os.path.join(HERE, code + '.jsonl')
        if not os.path.exists(path):
            continue
        SOURCES_PRESENT.add(code)
        with open(path, encoding='utf-8') as f:
            for line in f:
                line = line.strip()
                if not line:
                    continue
                e = json.loads(line)
                k = form_key(e.get('slp1', ''))
                if k:
                    idx[k][code].append(e.get('gloss', ''))
    return idx

def lookup(idx, key1, key2=None):
    hit = idx.get(form_key(key1))
    if not hit and key2:
        hit = idx.get(form_key(key2))
    hit = hit or {}
    indep = [{'source': NAME[c], 'code': c, 'gloss': g, 'publishable': publishable(c)}
             for c in INDEP for g in hit.get(c, [])[:MAX_GLOSS_PER_SOURCE]]
    kow = hit.get(REF, [])[:MAX_GLOSS_PER_SOURCE]
    return indep, kow

