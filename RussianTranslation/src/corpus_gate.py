#!/usr/bin/env python
"""pwg_ru stage-4 corpus gate — deterministic join + corpus query.

Joins a PWG headword (SLP1 key) to the five extracted Sanskrit→Russian
dictionaries in this directory and (optionally) to the SamudraManthanam
verse-aligned parallel corpus, assembling the input the LLM verdict prompt
(pwg_ru_prompts/4_korpus_proverka.txt) consumes. It also offers a cheap,
tunable heuristic correctness pre-check (the LLM is the real verdict).

This is CODE (committed); the dictionary data it reads (src/*.jsonl) is
gitignored and regenerated by build_src.py. The corpus query REUSES
SamudraManthanam's corpus.db (read-only) — it does not duplicate the corpus.

Modes:
  python corpus_gate.py lookup  <key1> [key2]      one headword → glosses + examples
  python corpus_gate.py card    <key1> "<pwg_ru>"  emit the 4_korpus input JSON + deterministic precheck
  python corpus_gate.py coverage [N]               per-signal coverage over PWG
  python corpus_gate.py tune     [N]               inter-dictionary agreement → threshold
"""
