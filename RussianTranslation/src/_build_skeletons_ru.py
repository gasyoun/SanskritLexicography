HEAD = {
 'anya':  ('anya «другой / иной»', 'non-samāsa'),
 'aksara':('akṣara «непреходящее / слог»', 'a-samāsa (nañ: a-+kṣara)'),
 'ananta':('ananta «бесконечный / Ананта»', 'a-samāsa (nañ: an-+anta)'),
}
missing = []
for w in ['anya', 'aksara', 'ananta']:
    sk = []
    for l in open(os.path.join(OUT, f'{w}.pd-min.md'), encoding='utf-8'):
        m = re.match(r'- \*\*(.+?)\*\* — (.+)', l.rstrip('\n'))
        if m: sk.append((m.group(1), clean(m.group(2))))
        else:
            m2 = re.match(r'- \*\*(.+?)\*\*\s*$', l.rstrip('\n'))
            if m2: sk.append((m2.group(1), ''))
    title, cls = HEAD[w]
    lines = [f'# {title} — PD, минимальный режим (билингва EN/RU)\n',
        f'_Скелет значений Деканского энциклопедического словаря (PD), {cls}, с русскими эквивалентами. '
        'Этимология и цитаты вынесены; полный PD — в файлах verbatim/IAST. Перевод глосс — черновой, требует сверки._\n',
        f'**{len(sk)} значений**\n', '| Значение | English (PD) | Русский |', '|:--|:--|:--|']
    for lbl, g in sk:
        if not g:
            lines.append(f'| **{lbl}** | | |'); continue
        ru = RU.get(g)
        if ru is None:
            missing.append((w, g)); ru = '—'
        lines.append(f'| **{lbl}** | {g} | {ru} |')
    # append corpus-grounded section
    frag = os.path.join(OUT, f'{w}.corpus-ru.md')
    if os.path.exists(frag):
        lines += ['', '---', '', open(frag, encoding='utf-8').read()]
    open(os.path.join(OUT, f'{w}.pd-min.ru.md'), 'w', encoding='utf-8').write('\n'.join(lines))
    print(f'{w}: {len(sk)} senses written')
print('MISSING:', len(missing))
for w, g in missing: print('  ', w, '::', repr(g))
